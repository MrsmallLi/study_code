#include <iostream>
#include <string>
using namespace std;


int main() {
    string a;
    cin >> a;
    string b;
    for (int i = 0; a[i]; i++) {
        if (a[i] != '2') continue;
        b = b + (to_string(2));
    }
    cout << b << endl;
    return 0;
}
